export function scenarioById(id: string | undefined): Scenario | undefined {
  return id ? scenarios.find((s) => s.id === id) : undefined;
}

/** Подсказки «что дальше» по сценарию (или общие, если сценарий не задан). */
const DEFAULT_FOLLOWUPS = [
  "Уточнить запрос",
  "Показать пример",
  "Начать новый сценарий",
];

export function followupsFor(scenarioId: string | undefined): string[] {
  return scenarioById(scenarioId)?.followups ?? DEFAULT_FOLLOWUPS;
}
